package com.example.javaproject3.week1.day4;

public class MultiplicationTable {

    public static void main(String[] args) {
        int num1 = 2;
        int num2 = 1;

        System.out.println(num1 + " * " + num2 + " = " + num1 * num2);
        System.out.printf("%d * %d = %d", num1, num2, num1 * num2);
    }
}
